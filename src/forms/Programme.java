    private void setValues(){
    pro.setId(Integer.parseInt(TextId.getText()));
    pro.setDestination(Textdestination.getText());
    SimpleDateFormat s = new SimpleDateFormat("yyyy-MM-dd");
    pro.setDate(String.valueOf(s.format(TextDate.getDate())));
    pro.setDuree(Integer.parseInt(Textduree.getText()));
    pro.setPrix(Double.parseDouble(TextPrix.getText()));
    }
