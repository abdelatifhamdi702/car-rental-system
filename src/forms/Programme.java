    private void tblProMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProMouseClicked
        // TODO add your handling code here:
         try{
         int row = tblPro.getSelectedRow();
         String id = tblPro.getValueAt(row, 0).toString();
         String destination = tblPro.getValueAt(row, 1).toString();
         String date = tblPro.getValueAt(row, 2).toString();
         String duree = tblPro.getValueAt(row, 3).toString();
         String prix = tblPro.getValueAt(row, 4).toString();
         TextId.setText(id);
         Textdestination.setText(destination);
         Textduree.setText(duree);
         SimpleDateFormat s = new SimpleDateFormat("yyyy-MM-dd");
         Date bd = s.parse(date);
         TextDate.setDate(bd);
         TextPrix.setText(prix);
         btnAjouter.setEnabled(false);
         btnModifier.setEnabled(true);
         btnSupprimer.setEnabled(true);
         }catch(Exception ex)
         {Message(ex.getMessage());} 
    }//GEN-LAST:event_tblProMouseClicked

