    private void btnRechercheActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRechercheActionPerformed
        String cherche ="select * from programme where ";
        if (RadBtnNum.isSelected()){ cherche+="id ="+TextRecherche.getText();}
        else if (RadBtnDestination.isSelected()){ cherche+="destination ='"+TextRecherche.getText()+"'";}
        else if (RadBtnDurée.isSelected()){ cherche+="duree ="+TextRecherche.getText();}
        else if (RdoDate.isSelected()){ cherche+="date ='"+TextRecherche.getText()+"'";}
        else if (RdoPrix.isSelected()){ cherche+="prix ="+TextRecherche.getText();}
        pro.getCustomRows(cherche, tblPro);

    }//GEN-LAST:event_btnRechercheActionPerformed

